package com.mycompany.java_practice_project.BeginnerJava;

public class FormetSpecifier {
    public static void main(String[] args) {
        
        boolean b = true;
        char c = 'a';
        short s = 32677;
        int i = 126587;
        float f = 10.20f;
        double d = 10.2111132154;


        System.out.printf("Boolean b = %b\n", b);
        //System.out.printf("Character c = %c\n",c);
        System.out.printf("Short s = %d\n",s);
        System.out.printf("Integer d = %d\n",i);
        System.out.printf("Float f = %.1f\n",f);
        System.out.printf("Float f = %.2f\n",d);


    }
}
